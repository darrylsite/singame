    @Override
    public void change()
    {
      EntityManager em = MyEntityManager.emf.createEntityManager();
      Query query = em.createQuery("select m from ReceiveMessages m");
      List liste = query.getResultList();
      if(liste.isEmpty())
          return;
      ReceiveMessages ms = (ReceiveMessages)liste.get(0);
      messages = ms.getMessages();
      popup.setMessages(messages);
      Object[][] data = new Object[messages.size()][4];
      int i=0;
      for(Message m : messages)
      {
       data[i][0] = m.getNumero();
       data[i][1] = m.getMessage();
       data[i][2] = m.getDates();
       data[i][3] = m.getStatus();
       i++;
      }
      this.msgDsp.refreshData(data);
    }

