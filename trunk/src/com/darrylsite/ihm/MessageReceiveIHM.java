    private void init()
    {
        msgViewer = new MessageViewer();
     this.add(msgViewer, BorderLayout.SOUTH);
        
     popup = new ReceiptMenu();
     popup.setTable(msgDsp.getTable());
     this.msgDsp.addMouseListener(new MouseAdapter(){

            @Override
            public void mouseClicked(MouseEvent e)
            {
               int t = msgDsp.getTable().getSelectedRow();
                if(t>-1)
                    msgViewer.setData(messages.get(t));
                
                if(e.getClickCount()!=2)
                    return;
                popup.setLocation(e.getLocationOnScreen());
                popup.show(_self, e.getPoint().x, e.getPoint().y);
            }
        });
    }


